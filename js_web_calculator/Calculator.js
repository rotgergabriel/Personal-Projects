class Calculator {
    sum(num1, num2) {
        return num1 + num2
    }

    rest(num1, num2) {
        return num1 - num2
    }

    split(num1, num2) {
        return num1 / num2
    }

    multiply(num1, num2) {
        return num1 + num2
    }

    percentage(percentageValue, num1) {
        return( (percentageValue/100) * num1 )
    }
}
